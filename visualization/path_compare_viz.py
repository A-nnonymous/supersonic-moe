def _render_frontier_contributions(report: dict[str, Any]) -> Path:
    """fig16 — redesigned: specific-sweep grouped bars + two heatmaps."""
    _apply_style()
    fig = plt.figure(figsize=(17.5, 12.4))
    gs = fig.add_gridspec(2, 2, height_ratios=[1.05, 1.0], hspace=0.34, wspace=0.26)
    ax_budget = fig.add_subplot(gs[0, 0])
    ax_dim    = fig.add_subplot(gs[0, 1])
    ax_mem_h  = fig.add_subplot(gs[1, 0])   # memory-delta T×I heatmap
    ax_ei_h   = fig.add_subplot(gs[1, 1])   # E×I heatmap

    _panel_label(ax_budget, "(a)")
    _panel_label(ax_dim,    "(b)")
    _panel_label(ax_mem_h,  "(c)")
    _panel_label(ax_ei_h,   "(d)")

    _plot_budget_waterfall(ax_budget, report["budget_anchor"])
    _plot_dimension_bars(
        ax_dim,
        report["frontier_summary"],
        report["precision_anchor"],
        report["grid_records"],
    )

    # (c) Memory-delta T×I averaged over E — built from grid_records
    grid_records = report["grid_records"]
    T_vals = sorted(set(int(r["T"]) for r in grid_records))
    I_vals = sorted(set(int(r["I"]) for r in grid_records))
    mat_mem = np.zeros((len(T_vals), len(I_vals)))
    for ti, T in enumerate(T_vals):
        for ii, I in enumerate(I_vals):
            vals = [float(r["peak_bwd_delta_pct"]) for r in grid_records
                    if int(r["T"]) == T and int(r["I"]) == I]
            mat_mem[ti, ii] = float(np.mean(vals)) if vals else 0.0
    mem_payload = {
        "values": mat_mem.tolist(),
        "rows": [f"T={t//1024}k" for t in T_vals],
        "cols": [f"I={i}" for i in I_vals],
    }
    _heatmap(
        ax_mem_h, mem_payload,
        "Memory delta  T×I  averaged over E",
        xlabel="Intermediate size I", ylabel="Tokens T",
        cmap="YlOrRd", fmt=".1f", annotate_suffix="%",
        cbar_label="Peak bwd memory Δ (%)",
    )
    ax_mem_h.text(
        0.5, -0.10,
        "Positive = FP8 uses more peak backward HBM than BF16  "
        "(wgrad quant temporaries)",
        transform=ax_mem_h.transAxes, ha="center", va="top",
        fontsize=7.8, color=C_NEUTRAL, style="italic",
    )

    # (d) E×I speedup heatmap averaged over T
    _heatmap(
        ax_ei_h,
        report["heatmaps"]["memory_delta_E_I_avg_over_T"],
        "Memory delta  E×I  averaged over T",
        xlabel="Intermediate size I", ylabel="Experts E",
        cmap="YlOrRd", fmt=".1f", annotate_suffix="%",
        cbar_label="Peak bwd memory Δ (%)",
    )

    fig.suptitle(
        "FP8 Frontier over BF16  —  Contribution Decomposition & Scaling Landscape",
        fontsize=16, fontweight="bold", y=0.992,
    )
    fig.text(
        0.5, 0.972,
        "Negative budget deltas = FP8 savings; positive = quant + bridge overhead.  "
        "Scaling bars use explicit fixed-parameter sweeps.",
        ha="center", fontsize=9, color=C_NEUTRAL,
    )
    return _save(fig, "fig16_fp8_frontier_contributions.png")


