def _save(fig: plt.Figure, name: str) -> Path:
    ASSETS.mkdir(parents=True, exist_ok=True)
    out = ASSETS / name
    fig.savefig(out, pad_inches=0.18)
    plt.close(fig)
    print(f"    -> {out}")
    return out


def _load(path: Path) -> dict[str, Any]:
    if not path.exists():
        raise FileNotFoundError(f"comparison report not found: {path}")
    return json.loads(path.read_text())


def _wrap(text: str, width: int) -> str:
    return textwrap.fill(text, width=width, break_long_words=False, break_on_hyphens=False)


def _fmt_mib(value: float) -> str:
    return f"{value:.0f} MiB"


def _fmt_us(value: float) -> str:
    return f"{value:+.0f} us"


def _panel_label(ax: plt.Axes, label: str) -> None:
    ax.text(
        -0.02, 1.055, label, transform=ax.transAxes,
        ha="left", va="bottom", fontsize=12, fontweight="bold", color="#334155",
    )


def _tensor_color(dtype: str) -> str:
    lower = dtype.lower()
    if "fp8" in lower:
        return "#FFEDD5"
    if "bf16" in lower or "bfloat16" in lower:
        return "#DBEAFE"
    return "#E2E8F0"


def _summarize_outputs(outputs: list[str], limit: int = 3) -> str:
    if len(outputs) <= limit:
        return ", ".join(outputs)
    return ", ".join(outputs[:limit]) + f", +{len(outputs) - limit} more"


def _summarize_tensors(tensors: list[dict[str, Any]], limit: int = 2) -> str:
    if not tensors:
        return "No unique mode tensor."
    parts = [f"{t['name']} {t['size_mib']:.0f} MiB" for t in tensors[:limit]]
    if len(tensors) > limit:
        parts.append(f"+{len(tensors) - limit} more")
    return ", ".join(parts)


def _note_box(ax: plt.Axes, x: float, y: float, w: float, h: float, text: str,
              facecolor: str, edgecolor: str, fontsize: float = 8.2) -> None:
    patch = FancyBboxPatch(
        (x, y), w, h,
        boxstyle="round,pad=0.008,rounding_size=0.015",
        facecolor=facecolor, edgecolor=edgecolor, linewidth=0.9,
    )
    ax.add_patch(patch)
    ax.text(x + w / 2, y + h / 2, _wrap(text, 28),
            ha="center", va="center", fontsize=fontsize, color=C_TEXT)


def _draw_stage_card(ax: plt.Axes, x: float, y: float, w: float, h: float,
                     stage: dict[str, Any], mode: str, stage_num: int) -> None:
    accent = C_BF16 if mode == "bf16" else C_FP8
    fill = C_COOL if mode == "bf16" else C_WARM
    memory = stage["manifest_memory"][mode]
    outputs = stage[f"{mode}_outputs"]
    tensors = stage.get("bf16_only_tensors", []) if mode == "bf16" else stage.get("fp8_only_tensors", [])
    tensor_summary = _summarize_tensors(tensors)
    tensor_fill = _tensor_color(tensors[0]["dtype"]) if tensors else "#F1F5F9"

    patch = FancyBboxPatch(
        (x, y), w, h,
        boxstyle="round,pad=0.010,rounding_size=0.020",
        facecolor=C_CARD, edgecolor=accent, linewidth=1.6,
    )
    ax.add_patch(patch)
    ax.add_patch(FancyBboxPatch(
        (x + 0.010, y + h - 0.095), w - 0.020, 0.070,
        boxstyle="round,pad=0.004,rounding_size=0.012",
        facecolor=fill, edgecolor="none",
    ))
    ax.text(x + 0.020, y + h - 0.060, f"{stage_num}. {stage['name']}",
            ha="left", va="center", fontsize=8.8, fontweight="bold", color=accent)

    op_text = _wrap(stage[f"{mode}_core"], 29)
    outputs_text = _wrap("Outputs: " + _summarize_outputs(outputs, limit=3), 30)
    memory_text = f"Alloc {memory['allocated_mib']:.0f} MiB | Peak {memory['peak_mib']:.0f} MiB"
    ax.text(
        x + 0.018, y + h - 0.120,
        op_text + "\n\n" + outputs_text + "\n\n" + memory_text,
        ha="left", va="top", fontsize=7.15, color=C_TEXT, linespacing=1.28,
    )

    chip_h = 0.102
    chip_y = y - 0.145
    chip = FancyBboxPatch(
        (x + 0.012, chip_y), w - 0.024, chip_h,
        boxstyle="round,pad=0.006,rounding_size=0.014",
        facecolor=tensor_fill, edgecolor="#BFDBFE" if mode == "bf16" else "#FDBA74",
        linewidth=0.8,
    )
    ax.add_patch(chip)
    ax.text(
        x + w / 2, chip_y + chip_h / 2,
        _wrap(tensor_summary, 24),
        ha="center", va="center", fontsize=6.7, color=C_TEXT, linespacing=1.16,
    )


def _draw_stage_card_v(ax: plt.Axes, x0: float, y0: float,
                       w: float, h: float,
                       stage: dict[str, Any], mode: str, stage_num: int) -> None:
    """Draw a compact wide card for vertical lane layout."""
    accent = C_BF16 if mode == "bf16" else C_FP8
    header_fill = C_COOL if mode == "bf16" else C_WARM
    memory = stage["manifest_memory"][mode]
    hdr_h = h * 0.33

    # Card border
    ax.add_patch(FancyBboxPatch(
        (x0, y0), w, h,
        boxstyle="round,pad=0.005,rounding_size=0.010",
        facecolor="#FAFBFC", edgecolor=accent, linewidth=1.5, zorder=2,
    ))
    # Header strip
    ax.add_patch(FancyBboxPatch(
        (x0, y0 + h - hdr_h), w, hdr_h,
        boxstyle="square,pad=0.0",
        facecolor=header_fill, edgecolor="none", zorder=3,
        clip_path=None,
    ))
    # Stage number circle
    r_chip = hdr_h * 0.38
    cx = x0 + r_chip + 0.012
    cy = y0 + h - hdr_h / 2
    ax.add_patch(plt.Circle((cx, cy), r_chip, color=accent, alpha=0.18, zorder=4))
    ax.text(cx, cy, str(stage_num),
            ha="center", va="center", fontsize=7.2, fontweight="bold",
            color=accent, zorder=5)
    # Stage name
    ax.text(cx + r_chip + 0.010, cy, stage["name"],
            ha="left", va="center", fontsize=7.6, fontweight="bold",
            color=accent, zorder=5)
    # Core op text
    core = _wrap(stage[f"{mode}_core"], 64)
    ax.text(x0 + 0.012, y0 + h - hdr_h - 0.008, core,
            ha="left", va="top", fontsize=5.9, color=C_TEXT,
            linespacing=1.18, zorder=4)
    # Memory line at bottom
    mem_str = (f"Alloc {memory['allocated_mib']:.0f} MiB  |  "
               f"Peak {memory['peak_mib']:.0f} MiB")
    ax.text(x0 + w - 0.010, y0 + 0.008, mem_str,
            ha="right", va="bottom", fontsize=5.5, color=C_NEUTRAL,
            style="italic", zorder=4)


def _draw_lane_v(ax: plt.Axes, stages: list[dict[str, Any]], mode: str) -> None:
    """Draw the 6 stages vertically (top-to-bottom) for fig15."""
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1)
    ax.axis("off")

    accent = C_BF16 if mode == "bf16" else C_FP8
    title = "BF16 Baseline Path" if mode == "bf16" else "FP8 Frontier Path"
    subtitle = (
        "Canonical BF16 operator flow with BF16 activation bridges."
        if mode == "bf16"
        else "Zero-materialization frontier: T-size quant + ZeroMat GEMM + FP8 bridges."
    )
    x0, card_w = 0.03, 0.94
    card_h = 0.109
    arrow_gap = 0.022
    slot_h = card_h + arrow_gap
    y_top_first = 0.900     # top edge of first card

    ax.text(0.50, 0.975, title, ha="center", va="top",
            fontsize=12, fontweight="bold", color=accent)
    ax.text(0.50, 0.950, subtitle, ha="center", va="top",
            fontsize=7.8, color=C_NEUTRAL, linespacing=1.25)

    # FWD / BWD section labels
    fwd_mid_y = y_top_first - 1.5 * slot_h    # midpoint of first 3 cards
    bwd_mid_y = y_top_first - 4.0 * slot_h    # midpoint of last 3 cards
    ax.text(-0.005, fwd_mid_y, "FWD", ha="right", va="center",
            fontsize=8, fontweight="bold", color=accent, alpha=0.65, rotation=90)
    ax.text(-0.005, bwd_mid_y, "BWD", ha="right", va="center",
            fontsize=8, fontweight="bold", color=accent, alpha=0.65, rotation=90)

    for i, stage in enumerate(stages):
        card_top = y_top_first - i * slot_h
        card_y0  = card_top - card_h          # bottom-left corner for Patch

        # FWD / BWD divider between stage 3 (i=2) and stage 4 (i=3)
        if i == 3:
            div_y = card_top + arrow_gap / 2
            ax.plot([0.03, 0.97], [div_y, div_y],
                    color="#94A3B8", linewidth=0.9, linestyle="--", zorder=1)
            lbl_w = 0.18
            ax.add_patch(FancyBboxPatch(
                (0.5 - lbl_w / 2, div_y - 0.012), lbl_w, 0.024,
                boxstyle="round,pad=0.003", facecolor="white",
                edgecolor="#94A3B8", linewidth=0.7, zorder=2,
            ))
            ax.text(0.5, div_y, "\u2193 BACKWARD",
                    ha="center", va="center", fontsize=6.8,
                    fontweight="bold", color=accent, alpha=0.85, zorder=3)

        # Downward arrow from previous card to this card
        if i > 0:
            arr_start_y = card_top + arrow_gap
            arr_end_y   = card_top + 0.003
            ax.add_patch(FancyArrowPatch(
                (0.5, arr_start_y), (0.5, arr_end_y),
                arrowstyle="-|>", mutation_scale=7,
                linewidth=0.9, color=accent, alpha=0.65, zorder=2,
            ))

        _draw_stage_card_v(ax, x0, card_y0, card_w, card_h, stage, mode, i + 1)

    # Footer note
    footer = (
        "BF16 keeps z and dz in BF16; TK-size HBM buffers are materialized."
        if mode == "bf16"
        else "Only T-size FP8 buffers used; gathered TK-size HBM is never materialized."
    )
    footer_fill = "#EFF6FF" if mode == "bf16" else "#FFF7ED"
    footer_edge = "#93C5FD" if mode == "bf16" else "#FDBA74"
    bot = y_top_first - 6 * slot_h + 0.002
    ax.add_patch(FancyBboxPatch(
        (x0, bot), card_w, 0.038,
        boxstyle="round,pad=0.005", facecolor=footer_fill,
        edgecolor=footer_edge, linewidth=0.8, zorder=2,
    ))
    ax.text(0.5, bot + 0.019, footer,
            ha="center", va="center", fontsize=6.5,
            color=C_TEXT, linespacing=1.15, zorder=3)

    footer = (
        "BF16 keeps z and dz in BF16 through the backward tail."
        if mode == "bf16"
        else "Only scales are gathered to TK; gathered TK-sized FP8 activations are never materialized in HBM."
    )
    footer_face = "#EFF6FF" if mode == "bf16" else "#FFF7ED"
    footer_edge = "#93C5FD" if mode == "bf16" else "#FDBA74"
    _note_box(ax, 0.28, 0.02, 0.44, 0.085, footer, footer_face, footer_edge, fontsize=8.1)


