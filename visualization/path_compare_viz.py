def _plot_phase_memory(ax: plt.Axes, report: dict[str, Any]) -> None:
    bf16 = report["path_analysis"]["phase_memory"]["bf16"]
    fp8 = report["path_analysis"]["phase_memory"]["fp8"]
    phase_names = ["Router", "UpProj Fwd", "DnProj Fwd", "DnProj Bwd", "UpBwd (wg)", "UpBwd (ag)"]
    x = np.arange(len(phase_names))

    bf16_alloc = np.array([row["allocated_mib"] for row in bf16], dtype=float)
    bf16_peak = np.array([row["peak_mib"] for row in bf16], dtype=float)
    fp8_alloc = np.array([row["allocated_mib"] for row in fp8], dtype=float)
    fp8_peak = np.array([row["peak_mib"] for row in fp8], dtype=float)

    ax.set_facecolor("#FCFCFD")
    ax.axvspan(0.75, 3.25, color="#FFF7ED", alpha=0.85)
    ax.text(2.0, max(fp8_peak) * 0.985, "FP8 quant / bridge frontier", ha="center", va="top",
            fontsize=8.8, color="#9A3412")

    ax.plot(x, bf16_alloc, marker="o", color=C_BF16, linewidth=2.1, label="BF16 allocated")
    ax.plot(x, bf16_peak, marker="o", color=C_BF16, linewidth=1.7, linestyle="--", label="BF16 peak")
    ax.plot(x, fp8_alloc, marker="s", color=C_FP8, linewidth=2.1, label="FP8 allocated")
    ax.plot(x, fp8_peak, marker="s", color=C_FP8, linewidth=1.7, linestyle="--", label="FP8 peak")

    bf16_idx = int(np.argmax(bf16_peak))
    fp8_idx = int(np.argmax(fp8_peak))
    ax.annotate(
        f"BF16 peak\n{_fmt_mib(bf16_peak[bf16_idx])}",
        xy=(bf16_idx, bf16_peak[bf16_idx]),
        xytext=(bf16_idx - 0.55, bf16_peak[bf16_idx] + 110),
        arrowprops=dict(arrowstyle="->", color=C_BF16, lw=1.0),
        fontsize=8.2, color=C_BF16, ha="left",
    )
    ax.annotate(
        f"FP8 peak\n{_fmt_mib(fp8_peak[fp8_idx])}",
        xy=(fp8_idx, fp8_peak[fp8_idx]),
        xytext=(fp8_idx + 0.15, fp8_peak[fp8_idx] + 135),
        arrowprops=dict(arrowstyle="->", color=C_FP8, lw=1.0),
        fontsize=8.2, color=C_FP8, ha="left",
    )

    ax.set_title("Phase-wise memory envelope from manifest.json", loc="left")
    ax.set_ylabel("MiB")
    ax.set_xticks(x)
    ax.set_xticklabels(phase_names, rotation=16, ha="right")
    ax.grid(True, axis="y", color=C_GRID)
    ax.legend(ncols=4, loc="upper left", frameon=False)


def _plot_budget_waterfall(ax: plt.Axes, budget: dict[str, Any]) -> None:
    rows = sorted(budget["budget_breakdown"], key=lambda row: row["delta_us"])
    labels = [LABEL_MAP.get(row["category"], row["category"]) for row in rows]
    vals = np.array([row["delta_us"] for row in rows], dtype=float)
    ypos = np.arange(len(rows))
    colors = [C_SAVE if v < 0 else C_COST for v in vals]

    pad = max(abs(vals.min()), abs(vals.max())) * 0.09 + 55
    ax.set_xlim(vals.min() - pad, vals.max() + pad)
    ax.set_facecolor("#FCFCFD")
    ax.barh(ypos, vals, color=colors, alpha=0.88, edgecolor="none")
    ax.axvline(0, color=C_TEXT, linewidth=1.0)
    ax.set_yticks(ypos)
    ax.set_yticklabels(labels)
    ax.set_xlabel("FP8 - BF16 (us / iter)")
    ax.set_title(f"Representative budget delta - {budget['shape_key']}", loc="left")
    ax.grid(True, axis="x", color=C_GRID)
    for y, value in zip(ypos, vals):
        ax.text(
            value + (14 if value >= 0 else -14), y, _fmt_us(value),
            va="center", ha="left" if value >= 0 else "right", fontsize=8,
        )
    net = budget["budget_totals"]["net_us"]
    ax.text(
        0.98, 0.03,
        f"Savings {budget['budget_totals']['savings_us']:.0f} us\n"
        f"Overhead {budget['budget_totals']['overhead_us']:.0f} us\n"
        f"Net {_fmt_us(net)}",
        transform=ax.transAxes,
        ha="right", va="bottom", fontsize=8.3,
        bbox=dict(boxstyle="round,pad=0.30", facecolor=C_PANEL, edgecolor="#CBD5E1"),
    )


def _compute_specific_sweeps(grid_records: list[dict[str, Any]]) -> dict[str, Any]:
    """Build 6 specific sweeps (2 per dimension) from the 27-shape grid."""
    lookup: dict[tuple[int, int, int], float] = {}
    for rec in grid_records:
        lookup[(int(rec["T"]), int(rec["E"]), int(rec["I"]))] = float(rec["speedup"])

    def _sweep(dim: str, values: list, fixed: dict[str, int]) -> list[float]:
        out = []
        for v in values:
            key_dict = dict(fixed)
            key_dict[dim] = v
            k = (key_dict["T"], key_dict["E"], key_dict["I"])
            out.append(lookup.get(k, float("nan")))
        return out

    return {
        "T_low_I":  {"vals": _sweep("T", [8192, 16384, 32768], {"E": 8, "I": 1536}),
                     "labels": ["8k", "16k", "32k"], "fixed": "E=8, I=1536"},
        "T_high_I": {"vals": _sweep("T", [8192, 16384, 32768], {"E": 8, "I": 3072}),
                     "labels": ["8k", "16k", "32k"], "fixed": "E=8, I=3072"},
        "I_low_T":  {"vals": _sweep("I", [1536, 2048, 3072], {"T": 8192,  "E": 8}),
                     "labels": ["1536", "2048", "3072"], "fixed": "T=8k, E=8"},
        "I_high_T": {"vals": _sweep("I", [1536, 2048, 3072], {"T": 32768, "E": 8}),
                     "labels": ["1536", "2048", "3072"], "fixed": "T=32k, E=8"},
        "E_low_T":  {"vals": _sweep("E", [8, 32, 128], {"T": 8192,  "I": 1536}),
                     "labels": ["8", "32", "128"], "fixed": "T=8k, I=1536"},
        "E_high_T": {"vals": _sweep("E", [8, 32, 128], {"T": 32768, "I": 3072}),
                     "labels": ["8", "32", "128"], "fixed": "T=32k, I=3072"},
    }


def _plot_dimension_bars(ax: plt.Axes, frontier_summary: dict[str, Any],
                         precision: dict[str, Any],
                         grid_records: list[dict[str, Any]]) -> None:
    """Paired grouped bars showing speedup sweeps for each dimension.
    Each dimension has two bars (two parameterizations) side-by-side.
    """
    sweeps = _compute_specific_sweeps(grid_records)

    # Layout: 3 dimension groups, each with 2 sub-sweeps, each with 3 values
    dim_groups = [
        ("Token count  T", [
            (sweeps["T_low_I"],  "#93C5FD", "E=8, I=1536"),
            (sweeps["T_high_I"], "#1D4ED8", "E=8, I=3072"),
        ]),
        ("Interm. size  I", [
            (sweeps["I_low_T"],  "#86EFAC", "T=8k, E=8"),
            (sweeps["I_high_T"], "#166534", "T=32k, E=8"),
        ]),
        ("Expert count  E", [
            (sweeps["E_low_T"],  "#FCA5A5", "T=8k, I=1536"),
            (sweeps["E_high_T"], "#991B1B", "T=32k, I=3072"),
        ]),
    ]

    xpos_all: list[float] = []
    vals_all: list[float] = []
    colors_all: list[str] = []
    tick_labels_all: list[str] = []
    group_spans: list[tuple[float, float, str]] = []
    legend_handles: list[Any] = []
    x = 0.0
    bar_w = 0.32

    for dim_name, sweeps_pair in dim_groups:
        group_start = x - 0.5
        s0, c0, lab0 = sweeps_pair[0]
        s1, c1, lab1 = sweeps_pair[1]
        n = len(s0["vals"])
        for k in range(n):
            xpos_all.append(x - bar_w / 2)
            xpos_all.append(x + bar_w / 2)
            vals_all.append(s0["vals"][k])
            vals_all.append(s1["vals"][k])
            colors_all.append(c0)
            colors_all.append(c1)
            tick_labels_all.append(s0["labels"][k])
            tick_labels_all.append("")
            x += 1.0
        group_spans.append((group_start, x - 0.52, dim_name))
        x += 0.80

        legend_handles.append(mpatches.Patch(color=c0, label=lab0))
        legend_handles.append(mpatches.Patch(color=c1, label=lab1))

    ax.set_facecolor("#FCFCFD")
    bg_colors = ["#EFF6FF", "#F0FDF4", "#FFF1F2"]
    for (start, end, label), bg in zip(group_spans, bg_colors):
        ax.axvspan(start, end, color=bg, alpha=0.80, zorder=0)
        ax.text((start + end) / 2, 1.025, label,
                transform=ax.get_xaxis_transform(),
                ha="center", va="bottom", fontsize=9, color=C_NEUTRAL,
                fontweight="bold")

    ax.bar(xpos_all, vals_all, width=bar_w * 0.90,
           color=colors_all, edgecolor="white", linewidth=0.4)

    mean_speed = frontier_summary["speedup_range"]["mean"]
    ax.axhline(mean_speed, color=C_TEXT, linestyle="--", linewidth=1.0,
               label=f"global mean {mean_speed:.2f}×")
    ax.set_ylabel("FP8 speedup (×)")
    ax.set_title(
        "Scaling: speedup under fixed-parameter sweeps  (paired: light=low, dark=high config)",
        loc="left", fontsize=9,
    )
    ax.set_xticks(xpos_all[::2])
    ax.set_xticklabels(tick_labels_all[::2])
    ax.set_ylim(1.00, max(v for v in vals_all if not np.isnan(v)) + 0.18)
    ax.grid(True, axis="y", color=C_GRID)
    for xp, val in zip(xpos_all, vals_all):
        if not np.isnan(val):
            ax.text(xp, val + 0.012, f"{val:.3f}×",
                    ha="center", va="bottom", fontsize=7.0)
    legend_handles.append(
        mpatches.Patch(color="none", label=f"global mean {mean_speed:.2f}×")
    )
    ax.legend(handles=legend_handles, frameon=True, framealpha=0.88,
              loc="upper left", fontsize=7.5, ncol=2)
    ax.text(
        0.99, 0.06,
        f"Precision anchor: {precision['shape_key']}\n"
        f"Max RRMSE {precision['max_rrmse_pct']:.2f}%  |  "
        f"Min cosine {precision['min_cosine_sim']:.5f}",
        transform=ax.transAxes,
        ha="right", va="bottom", fontsize=7.5,
        bbox=dict(boxstyle="round,pad=0.25", facecolor=C_PANEL, edgecolor="#CBD5E1"),
    )


def _heatmap(ax: plt.Axes, payload: dict[str, Any], title: str, xlabel: str, ylabel: str,
             cmap: str, fmt: str, annotate_suffix: str, cbar_label: str) -> None:
    data = np.array(payload["values"], dtype=float)
    im = ax.imshow(data, cmap=cmap, aspect="auto")
    ax.set_title(title, loc="left")
    ax.set_xticks(np.arange(len(payload["cols"])))
    ax.set_yticks(np.arange(len(payload["rows"])))
    ax.set_xticklabels(payload["cols"])
    ax.set_yticklabels(payload["rows"])
    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    ax.set_xticks(np.arange(-0.5, data.shape[1], 1), minor=True)
    ax.set_yticks(np.arange(-0.5, data.shape[0], 1), minor=True)
    ax.grid(which="minor", color="white", linestyle="-", linewidth=1.2)
    ax.tick_params(which="minor", bottom=False, left=False)

    lo = float(np.min(data))
    hi = float(np.max(data))
    mid = (lo + hi) / 2.0
    for i in range(data.shape[0]):
        for j in range(data.shape[1]):
            color = "white" if data[i, j] > mid else C_TEXT
            ax.text(
                j, i, format(data[i, j], fmt) + annotate_suffix,
                ha="center", va="center", fontsize=8.2, color=color,
            )
    cbar = plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    cbar.set_label(cbar_label, rotation=90)


